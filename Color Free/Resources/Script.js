function resolveLocale() {
  const language = (navigator.language || "en").toLowerCase().replace("-", "_");

  if (language.startsWith("zh")) {
    return "zh_CN";
  }

  if (language.startsWith("pt")) {
    return "pt_BR";
  }

  const supported = [
    "ar",
    "bn",
    "de",
    "en",
    "es",
    "fr",
    "hi",
    "id",
    "it",
    "ja",
    "ko",
    "ms",
    "pa",
    "ru",
    "tr",
    "ur",
  ];

  const matched = supported.find((locale) => language.startsWith(locale));
  return matched || "en";
}

function getCopy() {
  return translations[resolveLocale()] || translations.en;
}

function applyLocalizedCopy(useSettingsInsteadOfPreferences) {
  const copy = getCopy();
  const nextCopy = useSettingsInsteadOfPreferences
    ? {
        stateOnPreferences: copy.stateOnSettings,
        stateOffPreferences: copy.stateOffSettings,
        stateUnknown: copy.stateUnknownSettings,
        openPreferences: copy.openSettings,
      }
    : copy;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const value = nextCopy[key];

    if (value) {
      element.textContent = value;
    }
  });

  document.documentElement.lang = resolveLocale();
}

function show(enabled, useSettingsInsteadOfPreferences) {
  applyLocalizedCopy(useSettingsInsteadOfPreferences);

  if (typeof enabled === "boolean") {
    document.body.classList.toggle(`state-on`, enabled);
    document.body.classList.toggle(`state-off`, !enabled);
  } else {
    document.body.classList.remove(`state-on`);
    document.body.classList.remove(`state-off`);
  }
}

function openPreferences() {
  webkit.messageHandlers.controller.postMessage("open-preferences");
}

applyLocalizedCopy(false);
document
  .querySelector("button.open-preferences")
  .addEventListener("click", openPreferences);
